      Es war ein Herbstnach-mittag, als eine alter Freund und ich auf die Idee kamen, eine Weltkarte der anderen Art zu erschaffen. In 
        unserem kindlichen übermut wollten wir die gesamte Welt kartographieren, jeden Zusammenhang in der Geschichte der Menschheit 
        aufzeigen. 
      <br>
      <br> 
      Mit der Zeit kam Erfahrung und damit die Einsicht, dass ein solches Unterfangen ein Ding der Unmöglichkeit 
        ist. Fürs erste müsse es wohl genügen, sich auf das Selbst zu konzentrieren um im nächsten Schritt auf 
        die Erfahrungen anderer zu schließen. 
      <br>
      <br>
      Zumindest kleine Karten kann man zeichnen. War die Karte aller externen Verbindungen 
        unvorstellbar, so hatte die Idee einer Karte der internen Welt doch seinen Reiz. 
      <br>
      <br>
      Bedanken möchte ich mich herzlichst bei Friedrich Schöller, der für den Großteil der 
        Programmierung verwantwortlich ist. Außerdem natürlich bei der Magistratabteilung MA7, ohne die dieses Projekt nicht so 
        reibungsfrei ablaufen hätte können sowie bei meinen Lehrkräften der Universität für angewandte Kunst Wien - Abteilung digitale 
        Kunst.
