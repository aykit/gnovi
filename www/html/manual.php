      Mit gnovi werden textuelle Assoziationen auf spielerische Weise kartographiert. Dazu startet man viele 
        <a href="<?=htmlspecialchars(PageUrls::INPUT)?>">Anläufe</a>. Bei jedem 
        <a href="<?=htmlspecialchars(PageUrls::INPUT)?>">Anlauf</a> wird eines von vierhundert handverlesenen Worten 
        zufällig ausgespuckt. Nun bleiben zwanzig Sekunden Zeit um die dazu assoziierten Worte zu Bildschirm zu bringen. Danach 
        kommt es zur Eingabe der momentanen Lokalität. Auch hier bleiben wiederum zwanzig Sekunden Zeit, Assoziationen 
        aufzuschreiben. Im letzten Schritt geht es zur Konnotation der eingegebenen Worte, die mit den Pfeiltasten oder der Maus 
        erfolgt.
      <br>
      <br>
      Nach jedem <a href="<?=htmlspecialchars(PageUrls::INPUT)?>">Anlauf</a> wird die Assoziationskarte angezeigt. Ein Klick auf einen 
        Knotenpunkt zentriert diesen.  
        Wurde das ausgewählte Wort schon einmal eingegeben, so erscheint eine Zeitleiste, mit welcher Assoziationen zum jeweiligen 
        Zeitpunkt eingeblendet werden können. Alternativ dazu werden Assoziationskarten über die Links <a href="<?=htmlspecialchars
        (PageUrls::PERSONAL_GRAPH)?>">ich</a> und <a href="<?=htmlspecialchars(PageUrls::GLOBAL_GRAPH)?>">wir</a> erreicht. 
        <a href="<?=htmlspecialchars(PageUrls::PERSONAL_GRAPH)?>">Ich</a> dient dazu, allein die eigenen Assoziationen zu durchwandern. 
        Unter <a href="<?=htmlspecialchars(PageUrls::GLOBAL_GRAPH)?>">wir</a> hingegen werden die Assoziationen aller Teilnehmer_innen 
        angezeigt.
      <br>
      <br>
